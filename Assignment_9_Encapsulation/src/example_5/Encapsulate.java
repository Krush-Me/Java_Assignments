package example_5;

public class Encapsulate {

	private String Name;
	private int Roll;
	private int Age;
	
	public String getName() {
		return Name;
	}
	public int getRoll() {
		return Roll;
	}
	public int getAge() {
		return Age;
	}
	public void setName(String newName) {
		Name = newName;
	}
	public void setRoll(int newRoll) {
		Roll = newRoll;
	}
	public void setAge(int newAge) {
		Age = newAge;
	}
	
	
	
}
