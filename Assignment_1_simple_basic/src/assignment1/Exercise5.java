package assignment1;

// Variable assignment and increment and printing

public class Exercise5 {

	public static void main(String[] args) {
		
		int a = 10;
		System.out.println(a);
		
		a = a + 2;
		System.out.println(a);
		
		a = a * 2;
		System.out.println(a);
		
		a = a - 2;
		System.out.println(a);
		
		a = a / 2;
		System.out.println(a);
	}

}
