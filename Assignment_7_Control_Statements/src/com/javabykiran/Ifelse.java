package com.javabykiran;

public class Ifelse {
	public static void main(String[] args) {
		int num = 3;
		
		if (num == 1) {
			System.out.println("One");
		} else if (num == 2) {
			System.out.println("Two");
		} else if (num == 3) {
			System.out.println("Three");
		} else {
			System.out.println("Wrong input Given");
		}
	}
}
